package rpg.enums;

/**
 * El enum ItemType representa los diferentes tipos de ítems que
 * pueden existir en el juego, como armas, armaduras y misceláneos.
 */
public enum ItemType {
    WEAPON,  // Ítems de tipo arma
    ARMOR,   // Ítems de tipo armadura
    MISC     // Ítems misceláneos (curativos, objetos especiales, etc.)
}