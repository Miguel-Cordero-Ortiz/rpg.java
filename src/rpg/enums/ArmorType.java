/**
 * El enum ArmorType representa los diferentes tipos de armaduras,
 * cada uno con su posible posición y tipo de protección.
 */
public enum ArmorType {
    HELMET,      // Casco
    CHESTPLATE,  // Pechera
    LEGGINGS,    // Pantalones
    BOOTS,       // Botas
    SHIELD       // Escudo
}